buscando()
{
        echo -e "\033[01;32m"
        echo -e "###########################################"
        echo -e "|->          Buscando Portas..          <-|"
        echo -e "###########################################"
        echo -e "\033[00;00m"
}

# Chamada de função
apres

# Programa
if [ $# != 2 ]
then
	echo "# Usage $0 <ip> <portas.txt>"
else
	buscando
	echo -e "Host -> $1\n"

	for i in $(cat $2)
	do
		cmd=$(nc -vz $1 $i 2>/dev/null)
		
		if [ $? -eq 0 ]
		then
			echo -e "\033[01;36m# Porta $i: Aberta [o]\033[00;00m"
		fi
	done
fi
